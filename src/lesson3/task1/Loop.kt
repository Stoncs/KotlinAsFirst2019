 */
fun squareSequenceDigit(n: Int): Int {
    var length = 0
    var count = 0
    var k = 0
    var i = 1
    while (length < n) {
        k = sqr(i)
        count = 0
        while (k > 0) {
            k /= 10
            count++
        }
        length += count
